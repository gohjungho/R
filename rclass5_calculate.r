# 데이터 계산 

x <- c(1,2,3,4,5) / 4 # x를 4로 나눔
x

round(x,0) # 소수점 첫째 자리에서 반올림 

floor(x) # 소수점 이하를 내림 

ceiling(x) # 소수점 이하를 올림

trunc(x) # 소수점 아래 값 버림 

abs(-10) # 절댓값 

log(10, base = 2) # 밑이 2인 로그 

sqrt(10) # 제곱근 

exp(10) # 지수로 변환 

x <- c(1,2,3,4,5)
sum(x) # 합계 

mean(x) # 평균 

median(x) # 중앙값 

max(x) # 최대값 

min(x) # 최소값 

range(x) # 범위 

sd(x) # 표준편차 

var(x) # 분산 

